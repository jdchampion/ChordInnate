package chordinnate.exception;

public class ChordInnateIllegalArgumentException extends ChordInnateException {

    public ChordInnateIllegalArgumentException() {
        super();
    }

    public ChordInnateIllegalArgumentException(String s) {
        super(s);
    }

    public ChordInnateIllegalArgumentException(String message, Throwable cause) {
        super(message, cause);
    }

    public ChordInnateIllegalArgumentException(Throwable cause) {
        super(cause);
    }

}
